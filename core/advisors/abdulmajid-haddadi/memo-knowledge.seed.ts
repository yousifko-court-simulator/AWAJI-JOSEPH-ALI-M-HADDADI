export type CourtType =
  | "مدنية"
  | "تجارية"
  | "جزائية"
  | "إدارية"
  | "أحوال شخصية"
  | "عمالية"
  | "عقارية";

export type PartyRole = "مدعي" | "مدعى عليه" | "ممثل نظامي" | "وكيل" | "ولي" | "وصي";

export interface PartyIdentity {
  الاسم_الكامل: string;
  الجنسية?: string;
  رقم_الهوية_او_السجل?: string; // هوية/إقامة/سجل تجاري
  المهنة?: string;
  العنوان_الوطني?: string;
  وسائل_التواصل?: {
    جوال?: string;
    بريد_الكتروني?: string;
  };
}

export interface LegalRepresentative {
  الاسم: string;
  الصفة: PartyRole; // ممثل نظامي/وكيل...
  رقم_الوكالة?: string;
  تاريخ_الوكالة?: string; // هجري/ميلادي كما تريد
  صك_الولاية_للقاصرين?: string;
}

export interface ClaimBasics {
  اسم_المحكمة_المختصة: string;
  نوع_المحكمة: CourtType;
  الاختصاص_المكاني?: string;
  تاريخ_تقديم_الصحيفة: string;
  موضوع_الدعوى_موجز: string;
  الوقائع_والاسباب_مرتبة_زمنيا: string;
  الطلبات_بدقة: string[];
  المستندات_المؤيدة: string[];
  التوقيع: {
    اسم_الموقّع: string;
    صفته: PartyRole;
  };
}

export interface SpecialRequirements {
  // مدنية/عقارية
  الاهلية_القانونية?: {
    عمر_بالسنوات?: string; // استخدم أرقام عربية: "١٨"
    هل_بالغ?: boolean;
    هل_عاقل?: boolean;
    إن_كان_قاصرًا_فاذكر_الولي_او_الوصي?: string;
  };

  // عقارية
  عقارية?: {
    تحديد_العقار_بدقة?: {
      الموقع?: string;
      المساحة?: string;
      رقم_الصك?: string;
      الحدود_والمعالم?: string;
    };
    نوع_الطلبات_العقارية_الشائعة?: string[];
  };

  // تجارية
  تجارية?: {
    السجل_التجاري_للأطراف?: {
      سجل_المدعي?: string;
      سجل_المدعى_عليه?: string;
    };
    بيان_الرابط_بين_الطلبات_إن_تعددت?: string;
    مرفقات_تجارية_مفضلة?: string[];
    إنذار_مسبق_إن_لزم?: string;
    مدد_تقادم_إن_وجدت?: string;
  };

  // جزائية
  جزائية?: {
    دعوى_حق_خاص?: {
      هل_الضرر_ناشئ_عن_الجريمة?: boolean;
      وصف_الضرر?: string;
    };
    التقيد_بالمواعيد?: {
      مثال_قاعدة_الستين_يوما?: string; // "لا تُسمع ... بعد مضي (ستين) يوماً..."
    };
  };

  // إدارية (ديوان المظالم)
  إدارية?: {
    مقر_الجهة_المدعى_عليها?: string;
    صياغة_الاثباتات_بطريقة_قانونية?: string;
  };

  // ناجز
  ناجز?: {
    أهلية_مقدم_الطلب?: string[];
    طريقة_التقديم?: string;
    صيغة_المرفقات?: string; // PDF
  };
}

